<h3>活動系統簡介</h3>
<p>目前系學會的活動系統是利用kktix的系統來運作，kktix的優點、功能<a href="http://support.kktix.com/knowledgebase" target="_blank"><abbr title="官方說明">請看這裡</abbr></a></p>

<h3>使用步驟</h3>
<h4>先到KKtix註冊帳號</h4>

<h4>1.連線至kktix</h4>
<img src="<?=URL?>public/img/EventSystem/1.png" class="img-rounded img-responsive">
<p>到kktix首頁 <a href="http://kktix.com">KKtix首頁</a>，點選右上角的註冊（若已有帳號請按登入，並從<a href="#to-Aad">這裡</a>開始看）</p>

<h4>2.選擇用FB或填寫資料來註冊</h4>
<img src="<?=URL?>public/img/EventSystem/2.png" class="img-rounded img-responsive">
<p>使用FB註冊的話，信箱會直接使用FB註冊的信箱</p>


<h4>3.到信箱收認證信</h4>
<img src="<?=URL?>public/img/EventSystem/3.png" class="img-rounded img-responsive">

<h4>4.認證成功後，進入設定頁面</h4>
<img src="<?=URL?>public/img/EventSystem/4.png" class="img-rounded img-responsive">

<h4 id="to-Aad">5.通知學務長</h4>
<img src="<?=URL?>public/img/EventSystem/5.png" class="img-rounded img-responsive">
<p>將設定的信箱，告知學務長，等待學務長將您邀請至元智系學會組織內</p>
<p>如果已經在組織內，請點<a href="#createEvent">這裡</a></p>

<h4>6.至信箱接收邀請信</h4>
<img src="<?=URL?>public/img/EventSystem/6.png" class="img-rounded img-responsive">

<h4>7.點選同意加入系學會組織</h4>
<img src="<?=URL?>public/img/EventSystem/7.png" class="img-rounded img-responsive">

<h4>8.同意後自動轉至系學會組織頁面</h4>
<img src="<?=URL?>public/img/EventSystem/8.png" class="img-rounded img-responsive">

<h3 id="createEvent">建立活動</h3>
<h4>1.點選一下建立活動</h4>
<img src="<?=URL?>public/img/EventSystem/8.png" class="img-rounded img-responsive">
<p></p>
<h4>2.選擇建立新活動，或者選過往的活動直接其複製設定</h4>
<img src="<?=URL?>public/img/EventSystem/9.png" class="img-rounded img-responsive">
<p></p>
<h4>3.編輯活動資料</h4>
<img src="<?=URL?>public/img/EventSystem/10.png" class="img-rounded img-responsive">
<p>依照編輯活動資料，並將活動網址該欄的數值記下（如圖中紅框），這是系學會網站要抓資料用的重要資訊</p>
<p></p>

<h4>4.設定票種</h4>
<img src="<?=URL?>public/img/EventSystem/11.png" class="img-rounded img-responsive">
<p>票種設定頁面，大家可以把他想成參加身份別</p>
<p></p>

<h4>5.票種設定方法</h4>
<img src="<?=URL?>public/img/EventSystem/12.png" class="img-rounded img-responsive">
<p>設定票種資料，發售時間，可以視為報名時間，若是以小組報名，可以用販售數量限制組員人數，</p>
<p></p>

<h4>6.設定報名表單</h4>
<img src="<?=URL?>public/img/EventSystem/13.png" class="img-rounded img-responsive">
<p>最後是資料表單，首先是聯絡人和報名人的部份。聯絡人就是指填表單的人，報名人是指參加活動的人</p>
<p>若是小組報名的活動，可以設定要求報名人資料，要求填入所有組員的資料</p>
<p>接著是編輯表單內容，預設要求姓名和email（這兩樣不能刪除），左方有系統已經設定好的欄位供選擇，如果想加入其他的，可以按一下自訂</p>
<p></p>

<h4>7.發布活動</h4>
<img src="<?=URL?>public/img/EventSystem/14.png" class="img-rounded img-responsive">
<p>確定都設定好，按一下左邊的按鈕發布</p>

